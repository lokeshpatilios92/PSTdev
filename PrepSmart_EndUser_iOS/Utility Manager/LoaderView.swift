var loader: LoaderView! = LoaderView()

class LoaderView: UIView {

    var loader_View = UIView() //is going to be animated from bottom to top
    var fill_Image = UIImageView() //is going to mask everything with alpha mask

    override init(frame: CGRect) {
        super.init(frame: frame)
        setup()
    }

    required init?(coder aDecoder: NSCoder) {
        super.init(coder: aDecoder)
        setup()
    }

    func setup() {
        self.frame = CGRect.init(x: UIScreen.main.bounds.width/2 - 70/2, y: UIScreen.main.bounds.height/2 - 70/2, width: 70, height: 70)
        self.backgroundColor =   .orange
        //UIColor.black.withAlphaComponent(0.5)
        self.fill_Image.contentMode = .scaleAspectFill
        //self.fill_Image.image = UIImage(named: "glass")
        loader_View.frame = CGRect.init(x: 0, y: 0, width: 60, height: 60)
        loader_View.layer.cornerRadius = 10
        fill_Image.center = loader_View.center
        self.addSubview(loader_View)
        self.mask = fill_Image

        layoutIfNeeded()
        loader_View.frame.origin.y = bounds.height
    }

    override func layoutSubviews() {
        super.layoutSubviews()

        loader_View.frame = self.bounds
        fill_Image.frame = self.bounds
    }

    func animate(run: Bool) {
         if run == true
         {
            //self.fill_Image.image = UIImage.gifImageWithName("loader")
            self.fill_Image.image = UIImage(named: "user-name")
         }
         else
         {
            self.loader_View.frame.origin.y = self.bounds.height
            loader.removeFromSuperview()
         }
    }

    func showLoader() {
        DispatchQueue.main.async {
            self.loader_View.frame.origin.y = self.bounds.height
            UIApplication.shared.keyWindow?.addSubview(loader)
            self.animate(run: true)
        }
    }
    
    func hideLoader()
    {
       self.animate(run: false)
    }
}

